package nextnepal.com.test12123213;

public class PatroModel {

    private int year;
    private int month;
    private int day;
    private String id;
    private int dayOfWeek;
    private int startingDay;

    public int getYear() {
        return year;
    }

    void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    void setMonth(int month) {
        this.month = month;
    }

    int getDay() {
        return day;
    }

    void setDay(int day) {
        this.day = day;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getDayOfWeek() {
        return dayOfWeek;
    }

    void setDayOfWeek(int dayOfWeek) {
        this.dayOfWeek = dayOfWeek;
    }

    public int getStartingDay() {
        return startingDay;
    }

    void setStartingDay(int startingDay) {
        this.startingDay = startingDay;
    }
}
